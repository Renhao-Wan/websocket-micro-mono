package com.iot.websocket.service;

/**
 * 消息服务顶层接口
 *
 * 定义消息发布/订阅的核心操作，为不同的消息中间件实现提供统一接口。
 * 通过此接口，可以轻松切换消息服务的实现（如内存存储、Redis、Kafka等），
 * 而无需修改业务层代码，符合依赖倒置原则。
 *
 * 设计目标：
 * 1. 提供统一的消息服务抽象层
 * 2. 支持多种消息中间件的实现
 * 3. 实现业务逻辑与具体消息技术的解耦
 * 4. 便于测试和扩展
 *
 * 实现要求：
 * - 线程安全：所有实现必须是线程安全的，支持并发访问
 * - 可靠性：保证消息的可靠传递，不丢失消息
 * - 扩展性：支持动态添加/移除订阅者
 */
public interface MessageService {

    /**
     * 发布消息到指定频道
     *
     * 将消息发布到指定的频道，所有订阅了该频道的订阅者都会收到消息。
     * 此方法是同步操作，消息会立即发送给所有订阅者。
     *
     * @param topic 频道名称，用于标识消息的目标频道
     * @param message 要发布的消息内容，通常为字符串格式
     *
     * 使用场景：
     * 1. WebSocket控制器接收到客户端消息后，调用此方法发布消息
     * 2. REST API接收到外部系统请求后，发布通知消息
     * 3. 定时任务或其他业务逻辑需要广播消息时
     *
     * 实现要求：
     * - 如果topic为null或空，实现类应该抛出IllegalArgumentException
     * - 如果message为null，实现类可以决定是否接受空消息
     * - 实现应该保证消息至少被传递给所有当前已注册的订阅者
     */
    void publish(String topic, String message);

    /**
     * 订阅频道并转发到指定目的地
     *
     * 监听指定的频道，当收到消息时自动转发到指定的目的地。
     * 此方法建立了频道消息到目标地址的桥接关系。
     *
     * @param channelTopic 要监听的频道名称
     * @param destination 消息转发目的地，通常是WebSocket主题路径，如"/topic/greetings"
     *
     * 设计说明：
     * 1. 一个频道可以有多个订阅者，每个订阅者转发到不同的目的地
     * 2. 订阅关系在服务初始化时建立，通常持久化存在直到服务停止
     * 3. 消息转发应该是异步的，避免阻塞发布者线程
     *
     * 典型使用模式：
     * 1. 在@PostConstruct方法中调用此方法建立初始订阅关系
     * 2. 根据业务需求动态添加/移除订阅关系
     * 3. 将Redis频道、Kafka主题等消息源桥接到WebSocket目的地
     */
    void subscribe(String channelTopic, String destination);
}